export function getTemplateById(id: string): Template | undefined {
  return TEMPLATES.find(t => t.id === id);
}

export function getTemplatesByCategory(category: string): Template[] {
  if (category === 'all') return TEMPLATES;
  return TEMPLATES.filter(t => t.categories.includes(category as Template['categories'][0]));
}

export function getTopTemplates(count: number = 5): Template[] {
  return [...TEMPLATES].sort((a, b) => b.eqScore - a.eqScore).slice(0, count);
}
